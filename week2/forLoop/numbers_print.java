public class numbers_print{
    public static void main(String args[]){
        
        // Print numbers from -1111  to -1121
        System.out.println("Numbers from -1111 to -1121");
        for(int i=-1111;i>=-1121;i--){
            System.out.println(i);
        }

        //print numbers from 1234 to 1223
        System.out.println("Numbers from 1234 to 1223");
        for(int i=1234;i>=1223;i--){
            System.out.println(i);
        }
        //print numbers from -1111 to -1223
        System.out.println("Numbers from -1111 to -1223");
        for(int i=-1111;i>=-1223;i--){
            System.out.println(i);
        }
    }
}