public class char_print{
    public static void main(String args[]){
        // d to j
        System.out.println("Characters from d to j");
        for(char ch='d';ch<='j';ch++){
            System.out.println(ch);
        }

        //m to e
        System.out.println("Characters from m to e");
        for(char ch='m';ch>='e';ch--){
            System.out.println(ch);
        }

        //z to t
        System.out.println("Characters from z to t");
        for(char ch='z';ch>='t';ch--){ 
            System.out.println(ch);
        }

        //r to x
        System.out.println("Characters from r to x");
        for(char ch='r';ch<='x';ch++){
            System.out.println(ch);
        }
    
    }
}